int	ft_sqrt(int nb)
{
	int	result;
	int	i;

	result = 0;
	i = 2;
	if (nb <= 0)
	{
		result = 0;
	}
	if (nb == 1)
	{
		result = 1;
	}
	if (nb >= 2)
	{
		while ((i * i) <= nb)
		{
			if (i * i == nb)
			{
				result = i;
			}
			i++;
		}
	}
	return (result);
}
