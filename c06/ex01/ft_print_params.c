#include <unistd.h>

int	main(int argc, char **str)
{
	int	i;
	int	j;

	j = 1;
	while (argc > 1)
	{
		i = 0;
		while (str[j][i] != '\0')
		{
			write(1, &str[j][i], 1);
			i++;
		}
		write(1, "\n", 1);
		j++;
		argc--;
	}
	return (0);
}
